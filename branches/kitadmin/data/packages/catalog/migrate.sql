-- Definition of table `CATALOG_POSTS`
--

DROP TABLE IF EXISTS `CATALOG_POSTS`;
CREATE TABLE `CATALOG_POSTS` (
  `POST_ID` int(10) unsigned NOT NULL AUTO_INCREMENT,
  `POST_PRODUCTID` int(10) unsigned NOT NULL,
  `POST_POSTDATE` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  `POST_POST` varchar(255) NOT NULL DEFAULT '',
  `POST_AUTHOR` varchar(50) NOT NULL DEFAULT '',
  PRIMARY KEY (`POST_ID`),
  KEY `POST_PRODUCTID` (`POST_PRODUCTID`),
  CONSTRAINT `CATALOG_POSTS_ibfk_1` FOREIGN KEY (`POST_PRODUCTID`) REFERENCES `CATALOG_PRODUCTS` (`PRODUCTS_ID`) ON DELETE CASCADE ON UPDATE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8;

--
-- Definition of table `CATALOG_RATES`
--

DROP TABLE IF EXISTS `CATALOG_RATES`;
CREATE TABLE `CATALOG_RATES` (
  `RATE_ID` int(10) unsigned NOT NULL AUTO_INCREMENT,
  `RATE_PRODUCTID` int(10) unsigned NOT NULL,
  `RATE_RATE` tinyint(3) unsigned NOT NULL DEFAULT '0',
  `RATE_RATEDATE` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  `RATE_IP` int(10) unsigned NOT NULL DEFAULT '0',
  PRIMARY KEY (`RATE_ID`),
  KEY `RATE_PRODUCTID` (`RATE_PRODUCTID`),
  CONSTRAINT `CATALOG_RATING_ibfk_1` FOREIGN KEY (`RATE_PRODUCTID`) REFERENCES `CATALOG_PRODUCTS` (`PRODUCTS_ID`) ON DELETE CASCADE ON UPDATE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8;

--
-- Definition of table `CATALOG_RATINGS`
--

DROP TABLE IF EXISTS `CATALOG_RATINGS`;
CREATE TABLE `CATALOG_RATINGS` (
  `RATING_ID` int(10) unsigned NOT NULL AUTO_INCREMENT,
  `RATING_PRODUCTID` int(10) unsigned NOT NULL,
  `RATING_RATING` int(10) unsigned NOT NULL DEFAULT '0',
  `RATING_RATESCNT` int(10) unsigned NOT NULL DEFAULT '0',
  PRIMARY KEY (`RATING_ID`),
  KEY `RATING_PRODUCTID` (`RATING_PRODUCTID`),
  CONSTRAINT `CATALOG_RATINGS_ibfk_1` FOREIGN KEY (`RATING_PRODUCTID`) REFERENCES `CATALOG_PRODUCTS` (`PRODUCTS_ID`) ON DELETE CASCADE ON UPDATE CASCADE
) ENGINE=InnoDB AUTO_INCREMENT=2 DEFAULT CHARSET=utf8;

